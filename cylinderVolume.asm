#Rad EQU 0200H
#Hgt EQU 0201H
#Vol EQU 0202H

#Decimal EQU 0203H
#Fraction EQU 0204H

#PI_NUM EQU 16H
#PI_DEN EQU 07H

#ORG 0100H
#BEGIN 0100H


BEGIN:	MVI A,00H
	LXI H, Rad
	MOV M,A
	INX H
	MOV M,A
	INX H
	MOV M,A

READ_R: 	MVI A, 00H
	LXI H, Rad
	MOV M,A
	MOV B,M
	MOV C,M
	CALL MULTIPLY
	LXI H,Vol
	MOV M,B

READ_H: 	IN 01H
	LXI H,Hgt
	MOV M,A
	MOV C,M

	LXI H, Hgt
	MOV B,M
	CALL MULTIPLY
	MOV B,M
	MOV M,B

	MVI C,PI_NUM
	CALL MULTIPLY
	LXI H, Vol

	MOV D,B
	MOV M,B

	MVI E,  PI_DEN
	CALL DIVIDE
	LXI H, Decimal
	MOV M,C
	INX H
	MOV M,B

OUTPUT: 	MOV A,M
	OUT 10H
	LXI H, Decimal
	MOV A,M
	OUT 11H
	HLT

MULTIPLY: 	MVI A, 00H

MUL_SUB: 	ADD B
	DCR C
	JNZ MUL_SUB
	MOV B,A
	RET

DIVIDE: 	MVI A,00H
	MVI C,00H
	MOV A,D

DIV_SUB: 	SUB E
	INR C
	CMP E
	JNZ DIV_SUB
	MOV B,A
	RET
	
	